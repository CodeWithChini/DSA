a = 10
b = 18.9
c = "Hello World"
d = True
e = ['hello', 10, False]
f = (10, 20, 'hello')
g = {10, 20, 'hello'}
h = {'name': 'Hill', 'age': 20}

print(type(a))  # <class 'int'>
print(type(b))  # <class 'float'>
print(type(c))  # <class 'str'>
print(type(d))  # <class 'bool'>
print(type(e))  # <class 'list'>
print(type(f))  # <class 'tuple'>
print(type(g))  # <class 'set'>
print(type(h))  # <class 'dict'>

